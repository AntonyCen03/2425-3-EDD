    private void ACMPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ACMPActionPerformed
    String input1 =nodo1.getText();
    String input2 =nodo2.getText();
    if(input1.isBlank()==true || !Numero(input1) || input2.isBlank()==true || !Numero(input2))
    {
        JOptionPane.showMessageDialog(null,"Los datos ingresados no son validos intente nuevamente");
    }else
    {
        int val1=Integer.parseInt(input1);
        int val2= Integer.parseInt(input2);
        NodoABB aux1 = arbol.Buscar(val1,arbol.getRoot());
        NodoABB aux2 = arbol.Buscar(val2,arbol.getRoot());
        if(aux1!=null && aux2!=null && val1!=val2)
        {
            JOptionPane.showMessageDialog(null, "ACMP es: "+arbol.ACMP(val1,val2));
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Algunos de los Nodos no esta en el arbol o inserte numeros diferentes ");
        }
        
    nodo1.setText("");
    nodo2.setText("");
        
    }
    }//GEN-LAST:event_ACMPActionPerformed

