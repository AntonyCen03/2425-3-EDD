    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String input= agregado.getText();
    if(input.isBlank()==true || !Numero(input))
    {
        JOptionPane.showMessageDialog(null,"Los datos ingresados no son validos intente nuevamente");
    }else
    {
        
        int valor=Integer.parseInt(input);
        NodoABB aux = arbol.Buscar(valor,arbol.getRoot());
        if(aux==null)
        {
            if(arbol.EsVacio(arbol.getRoot()))
            {
                arbol.InsertarRoot(valor);
            }else
            {
                arbol.Insertar(valor,arbol.getRoot());
            }
             JOptionPane.showMessageDialog(null,"Valor agregado exitosamente");
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Ese valor ya se encuentra en el arbol");
        }
     
        agregado.setText("");
        
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

